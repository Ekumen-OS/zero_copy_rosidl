class TestUnboundedSequences:

    def test_sequence_fields(self):
        msg = UnboundedSequences()
        assert isinstance(msg.bool_values, Sequence)
        assert isinstance(msg.int32_values, Sequence)
        assert isinstance(msg.string_values, Sequence)

    def test_sequence_starts_empty(self):
        msg = UnboundedSequences()
        assert len(msg.bool_values) == 0
        assert len(msg.int32_values) == 0

    def test_sequence_append(self):
        msg = UnboundedSequences()
        msg.int32_values.append(42)
        msg.int32_values.append(99)
        assert len(msg.int32_values) == 2
        assert msg.int32_values[0] == 42

    def test_constraints_class(self):
        c = UnboundedSequences.Constraints()
        # Unbounded sequences should have SequenceConstraint fields
        assert hasattr(c, 'bool_values')
        assert isinstance(c.bool_values, SequenceConstraint)


