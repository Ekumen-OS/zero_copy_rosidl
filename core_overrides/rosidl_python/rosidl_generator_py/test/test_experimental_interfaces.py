# Defaults
# ---------------------------------------------------------------------------

class TestDefaults:

    def test_default_values_applied(self):
        msg = Defaults()
        # The Defaults message has fields with default annotations
        assert msg.bool_value.value is True
        assert msg.int8_value.value == -50
        assert msg.uint8_value.value == 200
        assert abs(msg.float32_value.value - 1.125) < 1e-5
        assert abs(msg.float64_value.value - 1.125) < 1e-9

    def test_defaults_only(self):
        msg = Defaults(_init=MessageInitialization.DEFAULTS_ONLY)
        assert isinstance(msg.bool_value, Scalar)
        assert msg.bool_value.value is True

    def test_zero_does_not_apply_defaults(self):
        msg = Defaults(_init=MessageInitialization.ZERO)
        # With ZERO, defaults should not be applied
        assert isinstance(msg.bool_value, Scalar)

    def test_skip(self):
        msg = Defaults(_init=MessageInitialization.SKIP)
        assert isinstance(msg.bool_value, Scalar)
