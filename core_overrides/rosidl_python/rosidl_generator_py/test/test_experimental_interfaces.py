"""Tests for generated experimental Python message classes.

These tests verify that the experimental messages generated by
rosidl_generator_py produce working classes backed by the experimental
container types from rosidl_runtime_py.experimental.
"""

import pytest

from rosidl_runtime_py.experimental import (
    Array,
    BoundedSequence,
    BoundedString,
    BoundedWString,
    Dtype,
    MessageInitialization,
    Scalar,
    Sequence,
    SequenceConstraint,
    String,
    StringConstraint,
    WString,
)


# Import generated experimental messages.
# These are generated from the same IDL files as the standard messages
# but under the experimental subpackage.
from rosidl_generator_py.msg.experimental import BasicTypes
from rosidl_generator_py.msg.experimental import Constants
from rosidl_generator_py.msg.experimental import Defaults
from rosidl_generator_py.msg.experimental import Strings
from rosidl_generator_py.msg.experimental import WStrings
from rosidl_generator_py.msg.experimental import Arrays
from rosidl_generator_py.msg.experimental import UnboundedSequences
from rosidl_generator_py.msg.experimental import BoundedSequences
from rosidl_generator_py.msg.experimental import Nested


# ---------------------------------------------------------------------------
# BasicTypes
# ---------------------------------------------------------------------------

class TestBasicTypes:

    def test_default_construction(self):
        msg = BasicTypes()
        # All scalar fields should be Scalar instances
        assert isinstance(msg.bool_value, Scalar)
        assert isinstance(msg.byte_value, Scalar)
        assert isinstance(msg.char_value, Scalar)
        assert isinstance(msg.float32_value, Scalar)
        assert isinstance(msg.float64_value, Scalar)
        assert isinstance(msg.int8_value, Scalar)
        assert isinstance(msg.uint8_value, Scalar)
        assert isinstance(msg.int16_value, Scalar)
        assert isinstance(msg.uint16_value, Scalar)
        assert isinstance(msg.int32_value, Scalar)
        assert isinstance(msg.uint32_value, Scalar)
        assert isinstance(msg.int64_value, Scalar)
        assert isinstance(msg.uint64_value, Scalar)

    def test_default_values_are_zero(self):
        msg = BasicTypes()
        assert msg.bool_value.value == False  # noqa: E712
        assert msg.float32_value.value == 0.0
        assert msg.int32_value.value == 0
        assert msg.uint64_value.value == 0

    def test_scalar_assignment(self):
        msg = BasicTypes()
        msg.int32_value.value = 42
        assert msg.int32_value.value == 42

    def test_equality(self):
        a = BasicTypes()
        b = BasicTypes()
        assert a == b
        a.int32_value.value = 99
        assert a != b

    def test_skip_initialization(self):
        msg = BasicTypes(_init=MessageInitialization.SKIP)
        # Should still be Scalar instances
        assert isinstance(msg.int32_value, Scalar)

    def test_constraints_class_exists(self):
        c = BasicTypes.Constraints()
        assert c == BasicTypes.Constraints()

    def test_repr(self):
        msg = BasicTypes()
        r = repr(msg)
        assert 'BasicTypes' in r


